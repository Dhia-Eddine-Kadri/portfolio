export async function aiExportToCourse(title, text, course, _folder) {
  if (!course) {
    window.showToast('No course', 'Open a course first');
    return;
  }
  var uid = window._currentUser && (window._currentUser.id || window._currentUser.sub);
  if (!uid) {
    window.showToast('Not logged in', 'Please sign in first');
    return;
  }
  if (_folder === undefined) {
    ufDestPicker(uid, course, function (picked) {
      aiExportToCourse(title, text, course, picked);
    });
    return;
  }
  var blob = await aiMakePdfBlob(title, text);
  if (!blob) return;
  var fname =
    title
      .slice(0, 50)
      .replace(/[^\w\s-]/g, '')
      .trim() + '.pdf';
  var file = new File([blob], fname, { type: 'application/pdf' });
  try {
    await window._ufUpload(uid, course, file, null, _folder || null);
    var dest = _folder
      ? '"' + _folder + '" in ' + (course.name || course.short)
      : course.name || course.short;
    window.showToast('Exported ✓', fname + ' added to ' + dest);
  } catch (e) {
    window.showToast('Export failed', e.message);
  }
}

export function aiShowExportModal(title, text) {
  var modal = document.getElementById('aiExportModal');
  var list = document.getElementById('aiExportCourseList');
  var status = document.getElementById('aiExportStatus');
  if (!modal) return;
  status.textContent = '';
  list.innerHTML = '';

  var allCourses = [];
  Object.values(window.SEMS || {}).forEach(function (sem) {
    (sem.courses || []).forEach(function (c) {
      allCourses.push(c);
    });
  });

  if (!allCourses.length) {
    list.innerHTML =
      '<p style="color:var(--on-glass-muted);font-size:.85rem">No courses found.</p>';
  } else {
    allCourses.forEach(function (course) {
      var btn = document.createElement('button');
      btn.style.cssText =
        'display:flex;align-items:center;gap:10px;background:rgba(255,255,255,.06);border:1px solid rgba(255,255,255,.1);border-radius:12px;padding:10px 14px;cursor:pointer;color:var(--on-glass);font-family:Nunito,sans-serif;font-size:.85rem;font-weight:700;text-align:left;transition:background .13s;width:100%';
      btn.innerHTML =
        '<span style="font-size:1.1rem">📚</span><span style="flex:1">' +
        (course.name || course.short) +
        '</span>';
      btn.addEventListener('mouseenter', function () {
        this.style.background = 'rgba(155,93,229,.2)';
      });
      btn.addEventListener('mouseleave', function () {
        this.style.background = 'rgba(255,255,255,.06)';
      });
      btn.addEventListener('click', function () {
        btn.disabled = true;
        var uid = window._currentUser && (window._currentUser.id || window._currentUser.sub);
        if (!uid) {
          status.textContent = '⚠️ Not signed in';
          return;
        }
        modal.style.display = 'none';
        ufDestPicker(uid, course, async function (folder) {
          modal.style.display = 'flex';
          status.textContent = '⏳ Uploading…';
          await aiExportToCourse(title, text, course, folder);
          status.textContent = '✓ Done!';
          setTimeout(function () {
            modal.style.display = 'none';
            status.textContent = '';
          }, 1200);
        });
      });
      list.appendChild(btn);
    });
  }

  modal.style.display = 'flex';
}

export function aiResponseActions(rawText, context) {
  var title = '';
  var headingMatch = rawText.match(/^#{1,3} (.+)/m);
  if (headingMatch) {
    title = headingMatch[1].trim();
  } else {
    var firstLine = rawText
      .replace(/^[\s\n]+/, '')
      .split('\n')[0]
      .replace(/\*\*/g, '')
      .trim();
    title = firstLine.length > 60 ? firstLine.slice(0, 60) + '…' : firstLine;
  }
  if (!title) title = 'AI Response';

  var bar = document.createElement('div');
  bar.className = 'ai-action-bar';

  var dlBtn = document.createElement('button');
  dlBtn.className = 'ai-action-btn';
  dlBtn.innerHTML =
    '<svg width="13" height="13" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.2" stroke-linecap="round" stroke-linejoin="round"><path d="M21 15v4a2 2 0 0 1-2 2H5a2 2 0 0 1-2-2v-4"/><polyline points="7 10 12 15 17 10"/><line x1="12" y1="15" x2="12" y2="3"/></svg> Download PDF';
  dlBtn.addEventListener('click', function () {
    aiDownloadPdf(title, rawText);
  });

  var expBtn = document.createElement('button');
  expBtn.className = 'ai-action-btn';
  expBtn.innerHTML =
    '<svg width="13" height="13" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.2" stroke-linecap="round" stroke-linejoin="round"><path d="M22 19a2 2 0 0 1-2 2H4a2 2 0 0 1-2-2V5a2 2 0 0 1 2-2h5l2 3h9a2 2 0 0 1 2 2z"/><line x1="12" y1="11" x2="12" y2="17"/><line x1="9" y1="14" x2="15" y2="14"/></svg> Export to course';
  expBtn.addEventListener('click', function () {
    if (context === 'panel') {
      aiExportToCourse(title, rawText, window.activeCourseRef);
    } else {
      aiShowExportModal(title, rawText);
    }
  });

  bar.appendChild(dlBtn);
  bar.appendChild(expBtn);
  return bar;
}
