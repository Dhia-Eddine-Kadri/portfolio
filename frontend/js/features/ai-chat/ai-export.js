export async function aiMakePdfBlob(title, text) {
  await window._ssEnsurePdfExportDeps();
  var jsPDF = (window.jspdf && window.jspdf.jsPDF) || window.jsPDF;
  if (!jsPDF) {
    window.showToast('jsPDF missing', 'Could not build PDF');
    return null;
  }
  if (!window.html2canvas) {
    window.showToast('html2canvas missing', 'Could not render PDF');
    return null;
  }

  var container = document.createElement('div');
  container.style.cssText =
    'position:fixed;left:-9999px;top:0;width:750px;background:#ffffff;color:#1a1a1a;font-family:Arial,Helvetica,sans-serif;font-size:13px;line-height:1.75;padding:44px 52px;box-sizing:border-box;z-index:99999;';

  var titleEl = document.createElement('div');
  titleEl.style.cssText =
    'font-size:19px;font-weight:700;color:#5028a0;margin-bottom:10px;padding-bottom:10px;border-bottom:2px solid #9b5de5;';
  titleEl.textContent = title;
  container.appendChild(titleEl);

  var contentEl = document.createElement('div');
  contentEl.style.color = '#1a1a1a';
  contentEl.innerHTML = window.renderMarkdown ? window.renderMarkdown(text) : text;
  container.appendChild(contentEl);

  var footer = document.createElement('div');
  footer.style.cssText =
    'margin-top:32px;font-size:10px;color:#aaa;border-top:1px solid #eee;padding-top:8px;';
  footer.textContent = 'Generated by StudySphere AI';
  container.appendChild(footer);

  document.body.appendChild(container);

  try {
    await new Promise(function (r) {
      setTimeout(r, 60);
    });
    var canvas = await html2canvas(container, {
      scale: 2,
      useCORS: true,
      backgroundColor: '#ffffff',
      logging: false
    });
    document.body.removeChild(container);

    var doc = new jsPDF({ unit: 'mm', format: 'a4' });
    var pageW = 210,
      pageH = 297,
      margin = 10;
    var imgW = pageW - margin * 2;
    var pageContentH = pageH - margin * 2;
    var pxPerMm = canvas.width / imgW;
    var srcYpx = 0;
    var firstPage = true;

    while (srcYpx < canvas.height) {
      if (!firstPage) doc.addPage();
      firstPage = false;
      var sliceHpx = Math.min(Math.round(pageContentH * pxPerMm), canvas.height - srcYpx);
      var sliceCanvas = document.createElement('canvas');
      sliceCanvas.width = canvas.width;
      sliceCanvas.height = sliceHpx;
      sliceCanvas
        .getContext('2d')
        .drawImage(canvas, 0, srcYpx, canvas.width, sliceHpx, 0, 0, canvas.width, sliceHpx);
      doc.addImage(
        sliceCanvas.toDataURL('image/png'),
        'PNG',
        margin,
        margin,
        imgW,
        sliceHpx / pxPerMm
      );
      srcYpx += sliceHpx;
    }

    return doc.output('blob');
  } catch (e) {
    if (document.body.contains(container)) document.body.removeChild(container);
    window.showToast('PDF error', e.message);
    return null;
  }
}

export async function aiDownloadPdf(title, text) {
  var blob = await aiMakePdfBlob(title, text);
  if (!blob) return;
  var url = URL.createObjectURL(blob);
  var a = document.createElement('a');
  a.href = url;
  a.download =
    title
      .slice(0, 60)
      .replace(/[^\w\s-]/g, '')
      .trim() + '.pdf';
  a.click();
  setTimeout(function () {
    URL.revokeObjectURL(url);
  }, 2000);
}

