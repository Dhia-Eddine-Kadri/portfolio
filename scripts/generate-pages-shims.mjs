// One-shot generator for Cloudflare Pages Functions shim files. Each shim
// just imports the existing Netlify-style handler from backend/functions/*
// and re-exports it through the pagesAdapter so route handling, body
// parsing, and response shape all map between the two runtimes.
//
// Run with:  node scripts/generate-pages-shims.mjs
//
// Re-running is idempotent — it overwrites the shim files but leaves the
// backend/functions/* handlers untouched.

import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');

// (url path under /api,   source basename in backend/functions/, optional rawBody mode)
const ROUTES = [
  ['ai',                              'ai'],
  ['suggestions',                     'suggestions'],
  ['create-checkout',                 'create-checkout'],
  ['stripe-webhook',                  'stripe-webhook',                  'base64'],
  ['paypal-webhook',                  'paypal-webhook',                  'base64'],
  ['admin-users',                     'admin-users'],
  ['admin/retrieval-logs',            'admin-retrieval-logs'],
  ['verify-payment',                  'verify-payment'],
  ['create-portal',                   'create-portal'],
  ['pause-subscription',              'pause-subscription'],
  ['resume-subscription',             'resume-subscription'],
  ['cancel-subscription',             'cancel-subscription'],
  ['reactivate-subscription',         'reactivate-subscription'],
  ['apply-retention-discount',        'apply-retention-discount'],
  ['activate-paypal-subscription',    'activate-paypal-subscription'],
  ['join-room-by-code',               'join-room-by-code'],
  ['send-chat-message',               'send-chat-message'],
  ['chat-friends',                    'chat-friends'],
  ['public-billing-config',           'public-billing-config'],
  ['chat-user-search',                'chat-user-search'],
  ['chat-username-check',             'chat-username-check'],
  ['report-chat-item',                'report-chat-item'],
  ['documents/upload',                'documents-upload'],
  ['documents/list',                  'documents-list'],
  ['documents/delete',                'documents-delete'],
  ['documents/index-existing',        'documents-index-existing'],
  ['documents/reindex-course',        'documents-reindex-course'],
  ['documents/review-pages',          'documents-review-pages'],
  ['documents/correct-page',          'documents-correct-page'],
  ['learning/topic-map',              'learning-topic-map'],
  ['learning/topic-map-generate',     'learning-topic-map-generate'],
  ['learning/next-action',            'learning-next-action'],
  ['ai/ask',                          'ai-ask'],
  ['ai/generate',                     'ai-generate'],
  ['ai/feedback',                     'ai-feedback'],
  ['ai/evaluate',                     'ai-evaluate'],
  ['ai/writing-coach',                'ai-writing-coach'],
  ['ai/usage',                        'ai-usage'],
  ['ai/quiz-attempt',                 'ai-quiz-attempt'],
  ['ai/mastery',                      'ai-mastery'],
  ['notes/generate',                  'notes-generate'],
  ['notes',                           'notes-crud'],
  ['chat-saved-replies',              'chat-saved-replies'],
  ['affiliate-dashboard',             'affiliate-dashboard']
];

const HEADER = '// AUTO-GENERATED by scripts/generate-pages-shims.mjs — do not edit by hand.\n';

for (const [urlPath, source, rawBody] of ROUTES) {
  const file = join(ROOT, 'functions', 'api', `${urlPath}.ts`);
  mkdirSync(dirname(file), { recursive: true });
  const depth = urlPath.split('/').length + 1;
  const up = '../'.repeat(depth);
  const adapterArg = rawBody ? `, { rawBody: '${rawBody}' }` : '';
  const body =
    HEADER +
    `import { handler } from '${up}backend/functions/${source}';\n` +
    `import { pagesAdapter } from '${up}backend/lib/pages-adapter';\n` +
    `\n` +
    `export const onRequest = pagesAdapter(handler${adapterArg});\n`;
  writeFileSync(file, body);
}

console.log(`Wrote ${ROUTES.length} shim files under functions/api/`);
